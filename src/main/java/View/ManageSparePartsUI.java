    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        IfNotEmpty();

        if ((SPNBezel.isSelected() == false) && (SPNCase.isSelected() == false) && (SPNCrown.isSelected() == false) 
                && (SPNCrystle.isSelected() == false) && (SPNDial.isSelected() == false) && (SPNHand.isSelected() == false) 
                && (SPNHourMarker.isSelected() == false)) {
            JOptionPane.showMessageDialog(this, "Please Select A Spare Part", "Spare Parts Management", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            Total = MSP.AddAction(SparePartName, GetSPQuantity());
            ModelTable = (DefaultTableModel) jTable1.getModel();
            ModelTable.addRow(new Object[]{
                SparePartName,
                SparePartID,
                txtSPQuantity.getText(),
                Total,});
            
            AvbSetText();
            Clear();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int RemovedQuantity = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
        Total = MSP.RemoveAction(SparePartName, RemovedQuantity);
        if (Total < 0) {
            JOptionPane.showMessageDialog(this, SparePartName + " Out-of-Stock", "Spare Parts Management", JOptionPane.ERROR_MESSAGE);
        } else {
            ModelTable.addRow(new Object[]{
                SparePartName,
                SparePartID,
                "(Deleted) " + RemovedQuantity,
                Total,});

            ModelTable.removeRow(jTable1.getSelectedRow());

            AvbSetText();
            Clear();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        IfNotEmpty();

        if ((SPNBezel.isSelected() == false) && (SPNCase.isSelected() == false) && (SPNCrown.isSelected() == false) 
                && (SPNCrystle.isSelected() == false) && (SPNDial.isSelected() == false) && (SPNHand.isSelected() == false) 
                && (SPNHourMarker.isSelected() == false)) {
            JOptionPane.showMessageDialog(this, "Please Select A Spare Part");
        } else {

            Total = MSP.RemoveAction(SparePartName, GetSPQuantity());
            if (Total < 0) {
                JOptionPane.showMessageDialog(this, SparePartName + " Out-of-Stock", "Spare Parts Management", JOptionPane.ERROR_MESSAGE);
            } else {
                ModelTable = (DefaultTableModel) jTable1.getModel();
                ModelTable.addRow(new Object[]{
                    SparePartName,
                    SparePartID,
                    "-" + txtSPQuantity.getText(),
                    Total,});

                AvbSetText();
                Clear();
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

