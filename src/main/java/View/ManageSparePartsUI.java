    public String IfNotEmpty() {
        if (txtSPQuantity.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill The Quantity", "Spare Parts Management", JOptionPane.INFORMATION_MESSAGE);
        } else {
            SparePartName = ButtonSelected();
            SparePartID = GiveID(SparePartName);
        }
        return null;
    }

    public int GetSPQuantity() {
        int SPQuantity = Integer.parseInt(txtSPQuantity.getText());
        return SPQuantity;
    }

    public String AvbSetText() {
        avbBezel.setText(Integer.toString(ManageSpareParts.TotalArr[0]));
        avbCase.setText(Integer.toString(ManageSpareParts.TotalArr[1]));
        avbCrown.setText(Integer.toString(ManageSpareParts.TotalArr[2]));
        avbCrystle.setText(Integer.toString(ManageSpareParts.TotalArr[3]));
        avbDial.setText(Integer.toString(ManageSpareParts.TotalArr[4]));
        avbHand.setText(Integer.toString(ManageSpareParts.TotalArr[5]));
        avbHourMarker.setText(Integer.toString(ManageSpareParts.TotalArr[6]));
        return null;
    }

    //Button SelectionHere
    public String ButtonSelected() {
        if (SPNBezel.isSelected() == true) {
            return "Bezel";
        } else if (SPNCase.isSelected() == true) {
            return "Case";
        } else if (SPNCrown.isSelected() == true) {
            return "Crown";
        } else if (SPNCrystle.isSelected() == true) {
            return "Crystle";
        } else if (SPNDial.isSelected() == true) {
            return "Dial";
        } else if (SPNHand.isSelected() == true) {
            return "Hand";
        } else if (SPNHourMarker.isSelected() == true) {
            return "Hour Marker";
        } else {
            return "INVALID";
        }
    }
    
    public String GiveID(String SparePartName) {
        if (SparePartName.equals("Bezel")) {
            return "SP001";
        } else if (SparePartName.equals("Case")) {
            return "SP002";
        } else if (SparePartName.equals("Crown")) {
            return "SP003";
        } else if (SparePartName.equals("Crystle")) {
            return "SP004";
        } else if (SparePartName.equals("Dial")) {
            return "SP005";
        } else if (SparePartName.equals("Hand")) {
            return "SP006";
        } else if (SparePartName.equals("Hour Marker")) {
            return "SP007";
        } else {
            return "INVALID";
        }
    }
    
