    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try
        {
            TTLoginUser LoginDetails = ObjLoginController.AddLoginUser(txtUsername.getText(), txtPassword.getText());
            TTLoginUser CheckLoginDetails = ObjLoginController.CheckUser(LoginDetails);
            
            if(CheckLoginDetails != null)
            {
                PaperClipsMainUI MainUI = new PaperClipsMainUI();
                MainUI.setVisible(true);
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Invalid Username or Password", "Error ", 0);
            }
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, "Details are Empty, ", "Error ", 0);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

